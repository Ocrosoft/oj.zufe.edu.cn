#include <stdio.h>
#include <stdlib.h>

int main()
{
    int a,n,i=0,t;
    long sum=0;
    while(scanf("%d %d",&a,&n)!=EOF)
    {
        i=0;
        t=a;
        sum =0;
        while (i<n)
        {
            sum+=t;
            t=t*10+a;
            i++;
        }
        printf("%ld\n",sum);
    }
    return 0;
}
